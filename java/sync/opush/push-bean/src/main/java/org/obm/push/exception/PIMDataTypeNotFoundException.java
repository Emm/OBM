package org.obm.push.exception;


public class PIMDataTypeNotFoundException extends Exception {
	
	public PIMDataTypeNotFoundException() {
		super();
	}

	public PIMDataTypeNotFoundException(String message, Throwable cause) {
		super(message, cause);
	}

	public PIMDataTypeNotFoundException(String message) {
		super(message);
	}

	public PIMDataTypeNotFoundException(Throwable cause) {
		super(cause);
	}
	
}
